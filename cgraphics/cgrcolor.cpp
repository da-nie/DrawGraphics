//----------------------------------------------------------------------------------------------------
//конструктор
//----------------------------------------------------------------------------------------------------
CGrColor::CGrColor(uint8_t r,uint8_t g,uint8_t b)
{
 SetColor(r,g,b);
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CGrColor::~CGrColor()
{  
}
//****************************************************************************************************
//закрытые функции
//****************************************************************************************************

//****************************************************************************************************
//открытые функции
//****************************************************************************************************

//----------------------------------------------------------------------------------------------------
//задать цвет в rgb
//----------------------------------------------------------------------------------------------------
void CGrColor::SetColor(uint8_t r,uint8_t g,uint8_t b)
{
 Color=r;
 Color<<=8;
 Color|=g;
 Color<<=8;
 Color|=b; 	
}
//----------------------------------------------------------------------------------------------------
//получить цвет в rgb
//----------------------------------------------------------------------------------------------------
void CGrColor::GetColor(uint8_t &r,uint8_t &g,uint8_t &b)
{
 b=(Color>>0)&0xff;
 g=(Color>>8)&0xff;
 r=(Color>>16)&0xff;
}

